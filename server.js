// ─── GitHub helpers ───────────────────────────────────────────────────────────

async function createGitHubRepo(repoName, token) {
  const res = await fetch('https://api.github.com/user/repos', {
    method: 'POST',
    headers: {
      'Authorization': `token ${token}`,
      'Accept': 'application/vnd.github.v3+json',
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      name: repoName,
      private: false,
      auto_init: false,
      description: 'GOV.UK prototype generated by Prototype Engine'
    })
  });
  if (!res.ok) {
    const err = await res.json();
    throw new Error(`GitHub repo creation failed: ${err.message}`);
  }
  return await res.json();
}

async function pushFileToGitHub(owner, repo, filePath, content, token) {
  const res = await fetch(`https://api.github.com/repos/${owner}/${repo}/contents/${filePath}`, {
    method: 'PUT',
    headers: {
      'Authorization': `token ${token}`,
      'Accept': 'application/vnd.github.v3+json',
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      message: `Add ${filePath}`,
      content: Buffer.from(content).toString('base64')
    })
  });
  if (!res.ok) {
    const err = await res.json();
    throw new Error(`Failed to push ${filePath}: ${err.message}`);
  }
  return await res.json();
}

async function pushAllFiles(owner, repoName, files, token) {
  for (const [filePath, content] of Object.entries(files)) {
    await pushFileToGitHub(owner, repoName, filePath, content, token);
    await new Promise(r => setTimeout(r, 200));
  }
}

